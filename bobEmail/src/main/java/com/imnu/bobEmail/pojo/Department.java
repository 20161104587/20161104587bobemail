package com.imnu.bobEmail.pojo;

public class Department {
    private Integer depid;

    private String depname;

    private String depdes;

    private String deprole;

    public Integer getDepid() {
        return depid;
    }

    public void setDepid(Integer depid) {
        this.depid = depid;
    }

    public String getDepname() {
        return depname;
    }

    public void setDepname(String depname) {
        this.depname = depname;
    }

    public String getDepdes() {
        return depdes;
    }

    public void setDepdes(String depdes) {
        this.depdes = depdes;
    }

    public String getDeprole() {
        return deprole;
    }

    public void setDeprole(String deprole) {
        this.deprole = deprole;
    }
}